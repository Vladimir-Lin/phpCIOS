  $this -> URL            = ""                                               ;
  $this -> Username       = ""                                               ;
  $this -> Password       = ""                                               ;
  $this -> Cust           = ""                                               ;
  $this -> CurrentCredits = 0                                                ;
  $this -> CurrentError   = ""                                               ;
  ////////////////////////////////////////////////////////////////////////////
  if                  ( ! array_key_exists ( $KEY , $GLOBALS )             ) {
    return false                                                             ;
  }                                                                          ;
  ////////////////////////////////////////////////////////////////////////////
  $CONF  = $GLOBALS   [ $KEY                                               ] ;
  $this -> ManagementURL  = $CONF [ "Management" ]                           ;
  $this -> LoginURL       = $CONF [ "Login"      ]                           ;
  $this -> URL            = $CONF [ "Hostname"   ]                           ;
  $this -> Username       = $CONF [ "Username"   ]                           ;
  $this -> Password       = $CONF [ "Password"   ]                           ;
  $this -> Cust           = $CONF [ "Cust"       ]                           ;
  ////////////////////////////////////////////////////////////////////////////
}
